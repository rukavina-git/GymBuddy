package com.rukavina.gymbuddy.data.model

/**
 * Enum representing the difficulty level of an exercise.
 * Used to help users select appropriate exercises based on their experience level.
 */
enum class DifficultyLevel {
    /** Suitable for those new to fitness or the specific exercise */
    BEGINNER,

    /** For those with some training experience and proper form */
    INTERMEDIATE,

    /** For experienced lifters with advanced strength and technique */
    ADVANCED
}
