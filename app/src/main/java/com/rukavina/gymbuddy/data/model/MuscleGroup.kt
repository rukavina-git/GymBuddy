package com.rukavina.gymbuddy.data.model

/**
 * Enum representing major muscle groups that can be targeted by exercises.
 * Used for categorizing exercises by primary and secondary muscle engagement.
 */
enum class MuscleGroup {
    /** Chest muscles (pectorals) */
    CHEST,

    /** Back muscles (lats, traps, rhomboids) */
    BACK,

    /** Leg muscles (quads, hamstrings, glutes, calves) */
    LEGS,

    /** Shoulder muscles (deltoids) */
    SHOULDERS,

    /** Arm muscles (biceps, triceps, forearms) */
    ARMS,

    /** Core muscles (abs, obliques, lower back) */
    CORE
}
